"use strict";

// Data types in JS
// Numbers => 2 to power 53
// bigINT
// String => collection of characters
// boolean => true/false
// symbol => unique
// null => standalone value
// undefined

// object

let name = "ALex";
let age = 21;
let isDriving = true;

console.log(name);
console.log(age);
console.log(isDriving);

// Using typeof function you can see the type of a variable
console.log(typeof name);
console.log(typeof age);
console.log(typeof isDriving);

console.log(typeof "Hitesh");
console.log(typeof null); // object
console.log(typeof undefined); // undefined