// Simple Comparison Operators
console.log(2>1); //true
console.log(2<1); //false
console.log(2>=1); //true
console.log(2<=1); //false
console.log(2!=1); //true
console.log(1==1); //true


// I'm surprised
console.log("2" > 1);
console.log("1" == 1);
// Automatically converts


// Lets play with them
console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);
// == and comparison works in different way where comparison compares the null which is same as 0 now.

console.log(undefined < 0);
console.log(undefined == 0);
console.log(typeof (undefined <= 0));

// ===------- Strictly checks datatypes and values
console.log(1 === 2);







